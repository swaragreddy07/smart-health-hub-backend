    public function storeAlert(Request $request){
        $request->validate([
            "user_id"=>"required",
            "prescription_id"=>"required",
            "preference"=>"required",
        ]);

        $alert = Alert::create([
            "user_id" => $request->user_id,
            "prescription_id" => $request->prescription_id,
            "preference" => $request->preference,
        ]);

        return response()->json(["alert"=>$alert]);
    }

